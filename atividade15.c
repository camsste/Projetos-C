#include <stdio.h>

int main(void) {
  int cont = 0;
  printf("Estrutura sequencial:\n\n");
  printf("Hello World\n");
  printf("Hello World\n");
  printf("Hello World\n");
  printf("Hello World\n");
  printf("Hello World\n");
  printf("Hello World\n");
  printf("Hello World\n");
  printf("Hello World\n");
  printf("Hello World\n");
  printf("Hello World\n");
  printf("Hello World\n");
  printf("\n\n");
  printf("Estrutura de repetição:\n\n");
  while(cont<=11){
    printf("Hello World\n");
    cont++;
  };
  return 0;
}
